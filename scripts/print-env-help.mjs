#!/usr/bin/env node
/**
 * Generate doc/ENV_REFERENCE.md from RAW_AGENT_* / EVOLUTION_* references in
 * source. We rely on a tiny grep + post-process pipeline:
 *   - Walk packages/, apps/, scripts/.
 *   - Collect every `RAW_AGENT_…` / `EVOLUTION_…` token and the file path(s)
 *     where it appears.
 *   - Emit a single markdown table sorted by name.
 *
 * The doc is intentionally a "where is this referenced" index — for human
 * descriptions edit `.env.example` (the existing semi-grouped commentary).
 *
 * Usage:  node scripts/print-env-help.mjs        # print to stdout
 *         node scripts/print-env-help.mjs --write # update doc/ENV_REFERENCE.md
 */
import { readFileSync, writeFileSync, mkdirSync, existsSync } from 'node:fs';
import { readdirSync, statSync } from 'node:fs';
import { dirname, join, relative } from 'node:path';
import { fileURLToPath } from 'node:url';

const repoRoot = join(dirname(fileURLToPath(import.meta.url)), '..');

const ROOTS = ['packages', 'apps', 'scripts'];
const FILE_EXT = /\.(ts|tsx|js|mjs|cjs|sh)$/;
const SKIP_DIR = new Set(['node_modules', 'dist', '.next', '.git', 'web-stub']);

const TOKEN = /\b(RAW_AGENT_[A-Z0-9_]+|EVOLUTION_[A-Z0-9_]+)\b/g;

function walk(dir, out) {
  for (const entry of readdirSync(dir)) {
    if (SKIP_DIR.has(entry)) continue;
    const p = join(dir, entry);
    let st;
    try { st = statSync(p); } catch { continue; }
    if (st.isDirectory()) walk(p, out);
    else if (FILE_EXT.test(entry)) out.push(p);
  }
}

function collect() {
  const occurrences = new Map(); // name → Set<relPath>
  for (const root of ROOTS) {
    const abs = join(repoRoot, root);
    if (!existsSync(abs)) continue;
    const files = [];
    walk(abs, files);
    for (const f of files) {
      let content;
      try { content = readFileSync(f, 'utf8'); } catch { continue; }
      const seen = new Set();
      for (const m of content.matchAll(TOKEN)) {
        const name = m[1];
        if (seen.has(name)) continue;
        seen.add(name);
        if (!occurrences.has(name)) occurrences.set(name, new Set());
        occurrences.get(name).add(relative(repoRoot, f));
      }
    }
  }
  return occurrences;
}

function render(occurrences) {
  const names = [...occurrences.keys()].sort();
  const rawAgent = names.filter((n) => n.startsWith('RAW_AGENT_'));
  const evolution = names.filter((n) => n.startsWith('EVOLUTION_'));

  const lines = [
    '<!-- Auto-generated by scripts/print-env-help.mjs — do not edit by hand. -->',
    '<!-- For descriptions / examples see .env.example. -->',
    '',
    '# Environment variable reference',
    '',
    `Generated: ${new Date().toISOString()}  ·  Total: ${names.length}` +
      ` (${rawAgent.length} runtime + ${evolution.length} evolution)`,
    ''
  ];

  function section(title, list) {
    lines.push(`## ${title}`, '');
    if (!list.length) {
      lines.push('_(none)_', '');
      return;
    }
    lines.push('| Name | Referenced in |', '| --- | --- |');
    for (const name of list) {
      const files = [...occurrences.get(name)].sort().slice(0, 6);
      const more = occurrences.get(name).size > 6 ? ` …(+${occurrences.get(name).size - 6})` : '';
      lines.push(`| \`${name}\` | ${files.map((f) => `\`${f}\``).join(', ')}${more} |`);
    }
    lines.push('');
  }

  section('RAW_AGENT_*', rawAgent);
  section('EVOLUTION_*', evolution);
  return lines.join('\n');
}

function main() {
  const occurrences = collect();
  const md = render(occurrences);
  if (process.argv.includes('--write')) {
    const docDir = join(repoRoot, 'doc');
    if (!existsSync(docDir)) mkdirSync(docDir, { recursive: true });
    const dest = join(docDir, 'ENV_REFERENCE.md');
    writeFileSync(dest, md, 'utf8');
    console.log(`wrote ${dest} (${occurrences.size} keys)`);
  } else {
    process.stdout.write(md + '\n');
  }
}

main();
